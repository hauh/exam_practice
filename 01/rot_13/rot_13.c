#include <unistd.h>

int	main(int argc, char **argv)
{
	char *p;

	if (argc == 2)
	{
		p = *(++argv);
		while (*p)
		{
			if ((*p >= 'A' && *p <= 'M') || (*p >= 'a' && *p <= 'm'))
				*p += 13;
			else if ((*p >= 'N' && *p <= 'Z') || (*p >= 'n' && *p <= 'z'))
				*p -= 13;
			++p;
		}
		write(1, *argv, p - *argv);
	}
	return (write(1, "\n", 1));
}
