#include <unistd.h>

int	main(int argc, char **argv)
{
	char *p;

	if (argc-- > 1)
	{
		p = *(argv += argc);
		while (*p)
			++p;
		write(1, *argv, p - *argv);
	}
	return (write(1, "\n", 1) - 1);
}
