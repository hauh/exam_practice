#include <unistd.h>

int	main(int argc, char **argv)
{
	char *p;

	if (argc == 2)
	{
		p = *(++argv);
		while (*p)
			++p;
		while (p != *argv)
			write(1, --p, 1);
	}
	return (write(1, "\n", 1));
}
