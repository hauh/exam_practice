#include <unistd.h>

int	main(int argc, char **argv)
{
	char *p;

	if (argc == 2)
	{
		p = *(++argv);
		while (*p)
		{
			if ((*p >= 'A' && *p < 'Z') || (*p >= 'a' && *p < 'z'))
				++(*p);
			else if (*p == 'Z' || *p == 'z')
				*p -= 25;
			++p;
		}
		write(1, *argv, p - *argv);
	}
	return (write(1, "\n", 1) - 1);
}
