#include <unistd.h>

int	main(int argc, char **argv)
{
	char *p0;
	char *p1;

	if (argc == 3)
	{
		p0 = *(++argv);
		p1 = *(argv + 1);
		while (*p0 && *p1)
		{
			if (*p1++ == *p0)
				++p0;
		}
		if (!*p0)
			write(1, *argv, p0 - *argv);
	}
	return (write(1, "\n", 1) - 1);
}
